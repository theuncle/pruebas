			<div class="modal-body">

				<p>Las cookies permiten a Anglo-Iberian Languages (en adelante AIL) almacenar y recuperar información.</p>

				<p>Una cookie es una pequeña información que se envía por un sitio web y se almacena en el navegador del usuario, así que el sitio web puede consultar la actividad previa de dicho usuario.</p>
				<p>Las cookies pueden caducar al final de una sesión de navegación (cuando se cierra el navegador) o después de un cierto período de tiempo.</p>

				<p>A continuación se describen las tecnologías que usamos y las medidas que puedes tomar para excluirte de estas funcionalidades:</p>

				<p>1. Los datos personales que recogemos en AIL a través de los formularios de contacto son almacenados en una base de datos que nos sirve para comunicarnos con el usuario así como para hacer campañas promocionales. El usuario tiene derecho a pedir la modificación o el borrado de sus datos en cualquier momento. Para hacer uso de su derecho nos puede contactar a través de ail@angloiberianlanguages.com</p>

				<p>2. AIL puede utilizar tecnologías de seguimiento para saber si sus usuarios vieron un anuncio en otro lugar y luego visitaron el sitio web.</p>

				<p>3. AIL también utiliza cookies para comprender, analizar y mejorar el producto. Para conseguirlo, trabajamos con socios terceros, como Google Analytics, que también colocan y leen cookies y otras tecnologías de seguimiento.</p>
				<hr>

				<h3>Cómo desactivar las cookies u otras tecnologías de seguimiento.</h3>
				<p>Cualquier usuario puede cambiar la configuración de su navegador para dejar de almacenar cookies. Es posible aceptar o rechazar el uso de cookies a través de una funcionalidad integrada en el navegador.</p>

				<p>Puedes obtener más información sobre la configuración de cookies en los siguientes enlaces:</p>

				<p>• Para Internet Explorer, haz clic <a href="http://windows.microsoft.com/es-xl/internet-explorer/delete-manage-cookies#ie=ie-10">aquí</a></p>
				<p>• Para Mozilla Firefox, haz clic <a href="https://support.mozilla.org/es/kb/cookies-informacion-que-los-sitios-web-guardan-en-">aquí</a></p>
				<p>• Para Google Chrome, haz clic <a href="https://support.google.com/chrome/answer/95647?hl=es">aquí</a></p>
				<p>• Para Safari, haz clic <a href="http://support.apple.com/kb/HT1677?viewlocale=es_ES">aquí</a></p>

				<p>En el caso de utilizar cualquier otro navegador, AIL recomienda consultar las distintas opciones de gestión de cookies de dicho navegador.</p>

